public class CaseConversion 
{
   public static void main( String args[] ) 
   {
      String str1 = "HELLO";
      System.out.println("String value = " + str1.toLowerCase());
      str1 = "GOOD";
      System.out.println("String value = " + str1.toLowerCase());

      String str2 = "java";
      System.out.println("String value = " + str2.toUpperCase());
      str2 = "python";
      System.out.println("String value = " + str2.toUpperCase());
   }

}